    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        
        try {
            PreparedStatement ps = cn.prepareStatement("INSERT INTO empleados (nombre,apellido,direccion,telefono)VALUES (?,?,?,?)");
            
            ps.setString(1, txtnombre.getText());
            ps.setString(2, txtapellidos.getText());
            ps.setString(3, txtdireccion.getText());
            ps.setString(4, txttelefono.getText());
            
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Datos guardados");
            
            limpiar();
            mostrarTabla("");
            
        } catch (SQLException e) {
            System.out.println("Error al guardar..."+e);
            JOptionPane.showMessageDialog(null, "Error al guardar");
        }
        
        
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        
        try {
            PreparedStatement ps=cn.prepareStatement("UPDATE empleados SET nombre='"+ txtnombre.getText()+"',apellido='"+txtapellidos.getText()+"',direccion='"+txtdireccion.getText()+"',telefono='"+txttelefono.getText()+"' WHERE id='"+txtid.getText()+"'");      
            
            int respuesta = ps.executeUpdate();
            
            if(respuesta>0){
                JOptionPane.showMessageDialog(null, "Datos actualizados");
                limpiar();
                mostrarTabla("");
            }else{
                
                JOptionPane.showMessageDialog(null, "No ha seleccionado fila");
            }
            
        } catch (Exception e) {
            System.out.println("Error al actualizar..."+e);
             JOptionPane.showMessageDialog(null, "Error al actualizar");
        }
        
        
    }//GEN-LAST:event_btnactualizarActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
       
        int fila = this.table.getSelectedRow();
        
        this.txtid.setText(this.table.getValueAt(fila, 0).toString());
        this.txtnombre.setText(this.table.getValueAt(fila, 1).toString());
        this.txtapellidos.setText(this.table.getValueAt(fila, 2).toString());
        this.txtdireccion.setText(this.table.getValueAt(fila, 3).toString());
        this.txttelefono.setText(this.table.getValueAt(fila, 4).toString());
        
    }//GEN-LAST:event_tableMouseClicked

